package com.dumbpug.dungeony.engine.ui;

public class UIElementDimensions {
    private float width, height;

    public UIElementDimensions(float width, float height) {
        this.width  = width;
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }
}
